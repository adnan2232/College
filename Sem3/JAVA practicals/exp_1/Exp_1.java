public class Exp_1{
	public static void main(String[] args){
		System.out.print("Even no: ");
		for(int i=2;i<=100;i++){
			if(i%2==0){
				System.out.print(i+"\t");
				}
			}		

		
		System.out.println("\n Odd no: ");
		for(int i=2;i<=100;i++){
			if(i%2!=0){
				System.out.print(i+"\t");
				}
			}		

		}

}